//FUNCIONES MATEMATICAS

//Valor absoluto de un numero (valor sin signo)

console.log(Math.abs(-45))

//potencias
//pow(base, exponente)
console.log(Math.pow(2,3))

//raiz cuadrada
//sqrt(num)

console.log(Math.sqrt(49))

//Redondear una cifra al mas cercano
//round(numero decimal)

console.log(Math.round(5.4))

//Numero Aleatorio
//Ramdom hasta el numero que le indiquemos
console.log(Math.round(Math.random()*20))
//con un rango indicado
console.log(Math.round(Math.random()*(10-5)+5))

//Si el numero es positivo o negativo
console.log(Math.sign(-45))

//redondea hacia el entero superior
console.log(Math.ceil(5.6))

//redondea hacia el entero inferior
console.log(Math.floor(8.9))